#Transformar una tupla a una lista
cool_tuple = (1, 2, 3)
cool_list = list(cool_tuple)
print(cool_list)



#Transformar una lista a una  tupla
cool_list = [1, 2, 3]
cool_tuple = tuple(cool_list)
print(cool_tuple)


#Transformar una string a una list
cool_string = "Hello"
cool_list = list(cool_string)
print(cool_list)


#Transformar una lista a un string
cool_list = ['H', 'e', 'l', 'l', 'o']
cool_string = str.join("", cool_list)
print(cool_string)
 
#str.join nos permite unir cada letra



